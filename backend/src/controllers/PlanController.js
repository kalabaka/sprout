  static async replan(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);
      const { keepCompleted = true, triggerReason = '用户手动触发' } = req.body;

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const beforeTasks = await LearningTaskModel.findByPlanId(planId, userId);
      const beforeTaskCount = beforeTasks.length;
      const beforeEndDate = await LearningTaskModel.getMaxPlannedDate(planId, userId);

      const userProfile = await ProfileModel.getProfile(userId);
      const masteredTopics = await MasteredTopicModel.getUserMasteredTopicIds(userId);

      const subject = PlanController.extractSubject(plan.goal || plan.name);

      const agentParams = {
        userId,
        target: plan.goal || plan.name,
        goalType: plan.goal_type || 'skill',
        subject: subject,
        userLevel: userProfile?.overall_level || 'beginner',
        masteredTopicIds: masteredTopics || [],
        learningPace: userProfile?.learning_pace || 'normal',
        dailyStudyMinutes: plan.daily_study_minutes || userProfile?.daily_available_minutes || 60,
        availableWeekdays: userProfile?.available_weekdays || [1, 2, 3, 4, 5, 6, 7],
        deadline: plan.end_date || plan.target_date,
        targetScore: plan.target_score,
        focusTopicIds: [],
        preferExercise: userProfile?.prefer_exercise !== false
      };

      const result = await PlanningAgent.generatePlan(agentParams);

      let completedTasks = [];
      if (keepCompleted) {
        completedTasks = await LearningTaskModel.findCompletedByPlanId(planId, userId);
      }

      const foundationTasks = identifyFoundationTasks(beforeTasks);
      const foundationNames = new Set(foundationTasks.map(t => t.name));
      
      logger.info(`识别到 ${foundationTasks.length} 个前置基础任务需要保留顺序`);

      const deletedCount = await LearningTaskModel.deleteByPlanId(planId, userId, keepCompleted);
      await PlanPhaseModel.deleteByPlanId(planId);

      let totalTasks = completedTasks.length;
      let totalMinutes = 0;
      let globalSequence = 1;

      for (let i = 0; i < result.plan.phases.length; i++) {
        const phaseData = result.plan.phases[i];
        
        if (!phaseData.tasks || !Array.isArray(phaseData.tasks)) {
          continue;
        }

        const phaseId = await PlanPhaseModel.create(planId, {
          name: phaseData.name,
          description: phaseData.description || null,
          phaseOrder: i + 1
        });

        for (let j = 0; j < phaseData.tasks.length; j++) {
          const taskData = phaseData.tasks[j];
          
          if (!taskData.name) {
            continue;
          }

          if (foundationNames.has(taskData.name)) {
            logger.info(`跳过已存在的前置任务: ${taskData.name}`);
            continue;
          }

          logger.info(`创建任务: name=${taskData.name}, scheduledDate=${taskData.scheduledDate}, plannedDate=${taskData.plannedDate}, category=${taskData.category}`);

          await LearningTaskModel.create(userId, {
            planId,
            phaseId,
            name: taskData.name,
            description: taskData.description || taskData.reason || null,
            plannedDuration: taskData.estimatedMinutes || 60,
            plannedDate: taskData.scheduledDate || taskData.plannedDate || null,
            sortOrder: globalSequence,
            difficulty: taskData.difficulty || 2,
            category: taskData.category || 'cognitive',
            needsReview: taskData.needsReview !== false
          });

          globalSequence++;
          totalTasks++;
          totalMinutes += taskData.estimatedMinutes || 60;
        }

        await PlanPhaseModel.updateTaskCount(phaseId);
      }

      const afterEndDate = await LearningTaskModel.getMaxPlannedDate(planId, userId);

      const ReplanLogModel = require('../models/ReplanLogModel');
      await ReplanLogModel.create({
        planId,
        triggerReason,
        beforeTasks: beforeTaskCount,
        afterTasks: totalTasks,
        adjustedDates: {
          beforeEndDate,
          afterEndDate
        }
      });

      await LearningPlanModel.update(planId, userId, {
        totalTasks,
        totalMinutes
      });

      await LearningPlanModel.updateStatus(planId, userId, 'active');

      logger.info(`重新规划: 计划${planId}, 阶段数${result.plan.phases.length}, 任务数${totalTasks}`);

      res.json(success({
        ...result,
        stats: {
          beforeTaskCount,
          afterTaskCount: totalTasks,
          preservedCompletedTasks: completedTasks.length,
          cancelledTasks: deletedCount
        }
      }, '重新规划成功'));
    } catch (error) {
      logger.error('重新规划失败:', error.message);
      res.status(500).json(fail('重新规划失败'));
    }
  }

  static async checkReplan(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const allTasks = await LearningTaskModel.findByPlanId(planId, userId);
      const completedTasks = allTasks.filter(t => t.status === 2);
      const pendingTasks = allTasks.filter(t => t.status === 0);
      const inProgressTasks = allTasks.filter(t => t.status === 1);
      const skippedTasks = allTasks.filter(t => t.status === 3);

      const completionRate = allTasks.length > 0 
        ? completedTasks.length / allTasks.length 
        : 0;

      const overdueTasks = allTasks.filter(t => t.is_overdue === 1);
      const lowScoreTasks = completedTasks.filter(t => t.self_score !== null && t.self_score < 60);

      const maxPlannedDate = await LearningTaskModel.getMaxPlannedDate(planId, userId);
      const planEndDate = plan.end_date || plan.target_date;

      let needReplan = false;
      let reasons = [];

      if (completionRate < 0.3 && allTasks.length > 5) {
        needReplan = true;
        reasons.push('完成率过低');
      }

      if (overdueTasks.length > 3) {
        needReplan = true;
        reasons.push('存在多个超时任务');
      }

      if (lowScoreTasks.length >= 3) {
        needReplan = true;
        reasons.push('多次学习效果不佳');
      }

      if (maxPlannedDate && planEndDate && new Date(maxPlannedDate) > new Date(planEndDate)) {
        needReplan = true;
        reasons.push('预计完成日期已超过目标日期');
      }

      const ReplanLogModel = require('../models/ReplanLogModel');
      const lastReplan = await ReplanLogModel.getLatest(planId);

      res.json(success({
        needReplan,
        reasons,
        stats: {
          totalTasks: allTasks.length,
          completedTasks: completedTasks.length,
          pendingTasks: pendingTasks.length,
          inProgressTasks: inProgressTasks.length,
          skippedTasks: skippedTasks.length,
          overdueTasks: overdueTasks.length,
          lowScoreTasks: lowScoreTasks.length,
          completionRate: Math.round(completionRate * 100)
        },
        dates: {
          maxPlannedDate,
          planEndDate,
          isOverdue: maxPlannedDate && planEndDate && new Date(maxPlannedDate) > new Date(planEndDate)
        },
        lastReplan
      }));
    } catch (error) {
      logger.error('检查重规划需求失败:', error.message);
      res.status(500).json(fail('检查失败'));
    }
  }

