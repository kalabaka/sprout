  static async getProgressTrend(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const startDate = plan.start_date || plan.created_at;
      const endDate = plan.end_date || plan.target_date;

      const sql = `
        SELECT 
          DATE(completed_at) as date,
          COUNT(*) as completed_count
        FROM learning_tasks
        WHERE plan_id = ? AND user_id = ? AND status = 2 AND completed_at IS NOT NULL
        GROUP BY DATE(completed_at)
        ORDER BY date ASC
      `;
      const [actualProgress] = await pool.execute(sql, [planId, userId]);

      const plannedTasks = await LearningTaskModel.findByPlanId(planId, userId);
      const totalTasks = plannedTasks.length;
      
      const plannedProgress = [];
      if (startDate && endDate && totalTasks > 0) {
        const start = new Date(startDate);
        const end = new Date(endDate);
        const days = Math.ceil((end - start) / (1000 * 60 * 60 * 24));
        const tasksPerDay = totalTasks / Math.max(days, 1);
        
        let cumulative = 0;
        for (let i = 0; i <= days; i++) {
          const date = new Date(start);
          date.setDate(date.getDate() + i);
          cumulative += tasksPerDay;
          plannedProgress.push({
            date: date.toISOString().split('T')[0],
            count: Math.min(Math.round(cumulative), totalTasks)
          });
        }
      }

      let cumulativeActual = 0;
      const actualProgressCumulative = actualProgress.map(item => {
        cumulativeActual += item.completed_count;
        return {
          date: item.date,
          count: cumulativeActual
        };
      });

      res.json(success({
        planned: plannedProgress,
        actual: actualProgressCumulative,
        totalTasks
      }));
    } catch (error) {
      logger.error('获取进度趋势失败:', error.message);
      res.status(500).json(fail('获取失败'));
    }
  }

