  static async updatePlanDeadline(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);
      const { endDate } = req.body;

      if (!endDate) {
        return res.status(400).json(fail('截止日期不能为空'));
      }

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const oldEndDate = plan.end_date || plan.target_date;

      await LearningPlanModel.update(planId, userId, { endDate });

      const timeDiff = new Date(endDate) - new Date(oldEndDate);
      const daysDiff = Math.ceil(timeDiff / (1000 * 60 * 60 * 24));

      logger.info(`更新计划截止日期: 计划${planId}, 旧日期${oldEndDate}, 新日期${endDate}, 差异${daysDiff}天`);

      res.json(success({
        oldEndDate,
        newEndDate: endDate,
        daysDiff
      }, '截止日期更新成功'));
    } catch (error) {
      logger.error('更新计划截止日期失败:', error.message);
      res.status(500).json(fail('更新失败'));
    }
  }

  static async addPlanTask(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);
      const { name, description, plannedDuration, plannedDate, phaseId, difficulty } = req.body;

      if (!name) {
        return res.status(400).json(fail('任务名称不能为空'));
      }

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const beforeStats = await LearningTaskModel.getTaskStatsByPlan(planId, userId);

      const taskId = await LearningTaskModel.createTask(userId, {
        planId,
        phaseId,
        name,
        description,
        plannedDuration: plannedDuration || 60,
        plannedDate,
        difficulty: difficulty || 2
      });

      const afterStats = await LearningTaskModel.getTaskStatsByPlan(planId, userId);

      const completionRateBefore = beforeStats.total > 0 
        ? Math.round((beforeStats.completed / beforeStats.total) * 100) 
        : 100;
      const completionRateAfter = afterStats.total > 0 
        ? Math.round((afterStats.completed / afterStats.total) * 100) 
        : 100;

      logger.info(`添加计划任务: 计划${planId}, 任务${taskId}, 名称${name}`);

      res.status(201).json(success({
        taskId,
        warning: {
          completionRateChanged: completionRateBefore !== completionRateAfter,
          completionRateBefore,
          completionRateAfter,
          message: completionRateAfter < completionRateBefore 
            ? `添加任务后，完成率将从 ${completionRateBefore}% 下降到 ${completionRateAfter}%` 
            : null
        }
      }, '任务添加成功'));
    } catch (error) {
      logger.error('添加计划任务失败:', error.message);
      res.status(500).json(fail('添加失败'));
    }
  }

  static async deletePlanTask(req, res) {
    try {
      const userId = req.user.userId;
      const planId = parseInt(req.params.id);
      const taskId = parseInt(req.params.taskId);

      const plan = await LearningPlanModel.findById(planId, userId);
      if (!plan) {
        return res.status(404).json(fail('计划不存在'));
      }

      const task = await LearningTaskModel.findById(taskId, userId);
      if (!task) {
        return res.status(404).json(fail('任务不存在'));
      }

      if (task.plan_id !== planId) {
        return res.status(400).json(fail('任务不属于该计划'));
      }

      if (task.status === 2) {
        return res.status(400).json(fail('已完成的任务不能删除'));
      }

      const cancelled = await LearningTaskModel.cancel(taskId, userId);

      if (cancelled) {
        logger.info(`取消计划任务: 计划${planId}, 任务${taskId}`);
        res.json(success(null, '任务已取消'));
      } else {
        res.status(400).json(fail('取消失败'));
      }
    } catch (error) {
      logger.error('取消计划任务失败:', error.message);
      res.status(500).json(fail('取消失败'));
    }
  }

